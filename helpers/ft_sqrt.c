#include "../push_swap.h"

int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 4)
		return (1);
	i = 2;
	while (i * i < nb)
		i++;
	if (i * i > nb)
	{
		if ((i * i - nb) < ((i - 1) * (i - 1) - nb))
			return (i);
	}
	return (i - 1);
}
